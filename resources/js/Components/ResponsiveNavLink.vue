<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/vue3';

const props = defineProps({
    href: {
        type: String,
        required: true,
    },
    active: {
        type: Boolean,
    },
});

const classes = computed(() =>
    props.active
        ? 'block w-full ps-3 pe-4 py-2 border-l-4 border-cyan-800 text-start text-base font-medium text-white bg-cyan-800 focus:outline-none focus:bg-cyan-100 focus:border-cyan-700 transition duration-150 ease-in-out'
        : 'block w-full ps-3 pe-4 py-2 border-l-4 border-transparent text-start text-base font-medium text-cyan-600 hover:text-white hover:bg-cyan-50 hover:border-cyan-800 focus:outline-none focus:text-white focus:bg-cyan-800 focus:border-cyan-300 transition duration-150 ease-in-out',
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
